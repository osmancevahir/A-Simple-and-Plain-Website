                aynı zamanda yedeğinin alınması, otomobilin üzerinde yer alan orijinal yazılımın Remap sayesinde düzenlenmesi, Remap işlemi uygulanan orijinal
                bilgilerin tekrar otomobil yazılımına yüklenmesidir.Yazılım değiştirme uygulaması iki farklı şekilde yapılmaktadır.Uygulanan ilk yöntem orijinal 

                <p>
                Euro 4 araçların bazılarında, Euro 5 ve Euro 6 araçların tümünde bulunan dizel partikül filtresi (DPF), emisyon standartlarını karşılamak için
                geliştirilen bir parçadır.Yapı olarak bal peteğine benzeyen bu filtre, egzozdaki çevreye zararlı katı partikülleri tutar ve bünyesinde hapseder.
                Ancak filtrede tutulan bu partiküllerin seviyesi zamanla artar ve tıkanma gibi sebeplere yol açabilir. 
                </p>

                <p>
                Bazı durumlarda araç rejenerasyon işlemini gerçekleştiremez ve kurum seviyesi motorun verimli çalışmasını engelleyecek hale gelir. Bu durumda
                gösterge ekranında DPF işareti belirebilir. Bu uyarı görüldüğünde araç sabit hızda ve yüksek devirde belirli bir süre kullanılmadır.
                Otomatik araçlarda ise bu işlem vites kolu manuel konuma alınarak yapılabilir. Aracın yüksek devirde kullanılmaya başlamasıyla motor yönetim sistemi
                daha fazla yakıt gönderir ve egzoz sıcaklığını arttırır. Egzoz sıcaklığının yaklaşık 600 °C’ye ulaşmasıyla rejenerasyon işlemi başlar ve katı
                partiküller yakılarak kuruma dönüştürülür. Bazı durumlarda ise bu işlem de bir işe yaramayabilir aracın DPF veya Motor Arıza Lambası sönmez ve araçta
                güç kaybı yaşanır. Böyle bir durumda araç servise götürülerek arıza tespit cihazı yardımıyla rejenerasyon işlemi yapılmalıdır.
                </p>

                <p>
                Genel olarak DPF aşağıdaki sebeplerden tıkanır:
                      Düşük kaliteli yakıt,
                      Düşük hızda ve dur-kalklı trafikte uzun süreli araç kullanımı,
                        Aracı rölantide uzun süre çalıştırmak,
                        Yanlış spesifikasyonda yağ kullanımı.
                Yeni çıkan tüm dizel araçlarda bulunan DPF, gerekli önlemlerin alınmaması durumunda rejenerasyon işlemi ile dahi temizlenemeyecek duruma gelebilir
                ve değişimi gerekebilir.
                </p>

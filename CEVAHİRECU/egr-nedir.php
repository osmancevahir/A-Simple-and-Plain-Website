                <p>
                Emme manifoldu üzerine monte edilen EGR solenoid valfı (dişli kutusu tarafı), motor kontrol ünitesi tarafından verilen komuta göre
                egzoz gazlarının emme manifolduna hareketini kontrol etme işlevine sahiptir. Egzoz gazındaki nitrojen oksitler, egzoz gazı devridaim
                sistemi kullanılarak  azaltılır. Egzoz gazının bir kısmı giriş havası ile karıştırılır. Böylece yanma odasındaki yanma reaksiyon hızı
                azalır ve yanma sıcaklığı düşer. Düşük yanma sıcaklığı, nitrojen oksit emisyonlarının azalmasını sağlar. Motor tam yükte çalışırken
                egzoz gazı geri çevirimi gerçekleşmez çünkü gücü artırmak için yanma odasında büyük miktarda oksijen bulunması gerekir.
                </p>

                <p>
                Genellikle EGR arızası sonrasında bir araba motorunun sarsılması veya  çalışmaması olarak kendini gösterebilir. Buna göre, yakıt
                tüketimi önemli ölçüde artacaktır. Zayıf çekiş, sarsıntılı hareket  veya zıplama veya tekleme gibi belirtiler  EGR valfinin kirli
                olduğunu gösterebilir. Zamanında müdahale etmezse, başka sorunların ortaya çıkması çok muhtemeldir. Bu nedenle EGR valfinin en
                kısa sürede temizlenmesi gerekir.
                </p>

                <p>
                Motorun emiş ve egzoz sisteminde tam olarak aktif olan subap, EGR arıza belirtilerinden biri olarak (genellikle egr supabı takılır
                veya açık kalır) hızlanma güçleşir ve yangınlar, patlamalar ve titreşimler meydana gelir. Ayrıca yakıt tüketimi önemli ölçüde artar
                ve varsa partikül filtresi dolar. Elbette tüm bunlara ek olarak aracınızda büyük ihtimalle sarı bir motor arıza ışığı yanacaktır.
                EGR valfi arızası belirtilerine dikkat etmek oldukça önemlidir.
                </p>
